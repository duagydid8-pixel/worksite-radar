// 한국 공휴일 (2025~2026)
const HOLIDAYS = new Set([
  // 2025
  "2025-01-01", // 신정
  "2025-01-28", "2025-01-29", "2025-01-30", // 설날연휴
  "2025-03-01", // 삼일절
  "2025-05-05", // 어린이날
  "2025-05-06", // 어린이날 대체
  "2025-05-13", // 부처님오신날
  "2025-06-06", // 현충일
  "2025-08-15", // 광복절
  "2025-10-03", // 개천절
  "2025-10-05", "2025-10-06", "2025-10-07", // 추석연휴
  "2025-10-08", // 추석 대체
  "2025-10-09", // 한글날
  "2025-12-25", // 성탄절
  // 2026
  "2026-01-01", // 신정
  "2026-01-28", "2026-01-29", "2026-01-30", // 설날연휴
  "2026-03-01", // 삼일절
  "2026-05-05", // 어린이날
  "2026-05-24", // 부처님오신날
  "2026-06-06", // 현충일
  "2026-08-15", // 광복절
  "2026-09-24", "2026-09-25", "2026-09-26", // 추석연휴
  "2026-10-03", // 개천절
  "2026-10-09", // 한글날
  "2026-12-25", // 성탄절
]);

export function isKoreanHoliday(year: number, month: number, day: number): boolean {
  const key = `${year}-${String(month).padStart(2, "0")}-${String(day).padStart(2, "0")}`;
  return HOLIDAYS.has(key);
}
